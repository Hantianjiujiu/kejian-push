/**
 * Prebuild script: bundle all markdown files into a single TS data file.
 * Run: node scripts/bundle-md.cjs
 */
const { readdirSync, readFileSync, writeFileSync } = require('fs')
const { resolve, join } = require('path')

const MD_DIR = resolve(__dirname, '../../课件文档-md')
const OUTPUT = resolve(__dirname, '../src/data/lessons-bundle.ts')

const files = readdirSync(MD_DIR).filter((f) => f.endsWith('.md'))

function preprocess(text) {
  return text.replace(/__(.+?)__/g, '**$1**')
}

const entries = []
for (const file of files.sort()) {
  const content = readFileSync(join(MD_DIR, file), 'utf-8')
  const clean = preprocess(content)
  const escaped = clean
    .replace(/\\/g, '\\\\')
    .replace(/`/g, '\\`')
    .replace(/\$/g, '\\$')
  entries.push(`  '${file}': \`${escaped}\``)
}

const ts = `// Auto-generated by scripts/bundle-md.cjs — do not edit manually
// Bundles all course markdown files as inline strings for static deployment

export const LESSON_DATA: Record<string, string> = {
${entries.join(',\n')}
}
`

writeFileSync(OUTPUT, ts, 'utf-8')
console.log(`Bundled ${files.length} markdown files → ${OUTPUT}`)
